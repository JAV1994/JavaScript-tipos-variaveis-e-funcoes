// Boolean: Usados para tomada de decisão.
// True: Verdadeiro.
// False: falso.

const numero1 = 1;
const numero2 = 2;


// == Atribuição Matemática
// === Comparação retorna verdadeiro e falso;
console.log("Numero 1 é igual em tipo e valor com o numero 2: " + numero1 === numero2)


// let permite a mudança da variavel e é mais segura que var
let teste = false;
teste = true;
console.log(teste);

